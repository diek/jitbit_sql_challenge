    [EmployeeID] INTEGER PRIMARY KEY AUTOINCREMENT NOT NULL,
    [DepartmentID] INTEGER NOT NULL,
    [BossID] INTEGER,
    [Name] TEXT  NOT NULL,
    [Salary] REAL,
    FOREIGN KEY([DepartmentID]) REFERENCES [department]([DepartmentID])
